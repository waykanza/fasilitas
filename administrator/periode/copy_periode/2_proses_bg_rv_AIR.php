(
	ID_PEMBAYARAN,
	TRX,
	
	PERIODE_TAG,
	PERIODE_AIR,
	TGL_JATUH_TEMPO,
	
	NO_PELANGGAN,
	
	KODE_SEKTOR,
	KODE_CLUSTER,
	KODE_BLOK,
