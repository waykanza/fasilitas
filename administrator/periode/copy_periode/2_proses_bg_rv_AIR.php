	(CAST(p.STATUS_BLOK AS VARCHAR(1)) + '#$periode_tag#' + p.NO_PELANGGAN) 
	NOT IN 
	(SELECT ID_PEMBAYARAN FROM KWT_PEMBAYARAN_AI_TEMP)
	
